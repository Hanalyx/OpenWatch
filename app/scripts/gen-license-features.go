//go:build ignore

// gen-license-features reads app/license/features.yaml and emits
// internal/license/features.gen.go: typed Feature constants + Metadata map.
//
// Run via `make generate-license` (or `go run scripts/gen-license-features.go`).
//
// Per app/specs/system/license-features.spec.yaml AC-1, AC-2, AC-3.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"strings"
	"text/template"

	"gopkg.in/yaml.v3"
)

const (
	inputPath  = "license/features.yaml"
	outputPath = "internal/license/features.gen.go"
)

type registry struct {
	Version    int       `yaml:"version"`
	Features   []feature `yaml:"features"`
	Deprecated []feature `yaml:"deprecated_features"`
}

type feature struct {
	ID          string `yaml:"id"`
	Tier        string `yaml:"tier"`
	Description string `yaml:"description"`
	Introduced  string `yaml:"introduced"`
}

const fileTpl = `// Code generated by scripts/gen-license-features.go from app/license/features.yaml.
// DO NOT EDIT. Re-run via: make generate-license

package license

// Feature is a stable identifier for a license-gated capability.
type Feature string

// Tier is the license tier required to access a feature.
type Tier string

const (
	TierFree         Tier = "free"
	TierOpenWatchPlus Tier = "openwatch_plus"
	TierEnterprise   Tier = "enterprise"
)

// Feature constants. Hand-typed feature-string literals are forbidden by lint;
// use these constants.
const (
{{- range .Features }}
	// {{ .Description }}
	{{ .GoConst }} Feature = "{{ .ID }}"
{{- end }}
)

// FeatureMeta is the per-feature metadata derived from features.yaml.
type FeatureMeta struct {
	ID          Feature
	Tier        Tier
	Description string
	Introduced  string
}

// FeatureRegistry maps every active feature id to its registry entry.
var FeatureRegistry = map[Feature]FeatureMeta{
{{- range .Features }}
	{{ .GoConst }}: {
		ID:          {{ .GoConst }},
		Tier:        Tier{{ .GoTier }},
		Description: ` + "`" + `{{ .Description }}` + "`" + `,
		Introduced:  "{{ .Introduced }}",
	},
{{- end }}
}

// AllFeatures returns every active feature id in registry declaration order.
func AllFeatures() []Feature {
	out := make([]Feature, 0, len(featureOrder))
	out = append(out, featureOrder...)
	return out
}

var featureOrder = []Feature{
{{- range .Features }}
	{{ .GoConst }},
{{- end }}
}

// IsKnown reports whether the supplied id matches a registered feature.
// Hot-path callers (IsEnabled) skip this; it's for /admin/license:verify.
func IsKnown(f Feature) bool {
	_, ok := FeatureRegistry[f]
	return ok
}
`

type emitContext struct {
	Features []emitFeature
}

type emitFeature struct {
	ID          string
	GoConst     string
	Tier        string
	GoTier      string
	Description string
	Introduced  string
}

func main() {
	raw, err := os.ReadFile(inputPath)
	check(err, "read input")

	var reg registry
	check(yaml.Unmarshal(raw, &reg), "parse yaml")

	if len(reg.Features) == 0 {
		fail("no features in registry")
	}

	ec := emitContext{Features: make([]emitFeature, 0, len(reg.Features))}
	for _, f := range reg.Features {
		ec.Features = append(ec.Features, enrich(f))
	}

	tpl, err := template.New("features").Parse(fileTpl)
	check(err, "parse template")

	var buf bytes.Buffer
	check(tpl.Execute(&buf, ec), "execute template")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "%s\n---\n", buf.String())
		check(err, "gofmt")
	}

	check(os.WriteFile(outputPath, formatted, 0o644), "write output")
	fmt.Printf("wrote %s (%d features)\n", outputPath, len(ec.Features))
}

func enrich(f feature) emitFeature {
	return emitFeature{
		ID:          f.ID,
		GoConst:     toPascal(f.ID),
		Tier:        f.Tier,
		GoTier:      tierToPascal(f.Tier),
		Description: cleanDescription(f.Description),
		Introduced:  f.Introduced,
	}
}

func toPascal(id string) string {
	parts := strings.FieldsFunc(id, func(r rune) bool { return r == '_' || r == '.' })
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		b.WriteString(p[1:])
	}
	return b.String()
}

func tierToPascal(t string) string {
	switch t {
	case "free":
		return "Free"
	case "openwatch_plus":
		return "OpenWatchPlus"
	case "enterprise":
		return "Enterprise"
	default:
		return "Free"
	}
}

func cleanDescription(s string) string {
	s = strings.TrimSpace(s)
	if s == "" {
		return ""
	}
	return strings.Join(strings.Fields(s), " ")
}

func check(err error, what string) {
	if err != nil {
		fail("%s: %v", what, err)
	}
}

func fail(format string, args ...interface{}) {
	fmt.Fprintf(os.Stderr, "gen-license-features: "+format+"\n", args...)
	os.Exit(1)
}
