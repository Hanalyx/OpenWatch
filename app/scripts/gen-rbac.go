//go:build ignore

// gen-rbac reads app/auth/permissions.yaml and emits two files:
//
//	internal/auth/permissions.gen.go — typed Permission constants + metadata map
//	internal/auth/roles.gen.go       — typed RoleID constants + BuiltInRoles (wildcards expanded)
//
// Run via `make generate-rbac` (or `go run scripts/gen-rbac.go`).
//
// Per app/specs/system/rbac.spec.yaml AC-01, AC-02, AC-03, AC-04, C-07.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"sort"
	"strings"
	"text/template"

	"gopkg.in/yaml.v3"
)

const (
	inputPath       = "auth/permissions.yaml"
	permissionsPath = "internal/auth/permissions.gen.go"
	rolesPath       = "internal/auth/roles.gen.go"
)

type registry struct {
	Version     int          `yaml:"version"`
	Categories  []category   `yaml:"categories"`
	Permissions []permission `yaml:"permissions"`
	Deprecated  []permission `yaml:"deprecated_permissions"`
	Roles       []role       `yaml:"roles"`
}

type category struct {
	ID          string `yaml:"id"`
	Description string `yaml:"description"`
}

type permission struct {
	ID           string `yaml:"id"`
	Category     string `yaml:"category"`
	Description  string `yaml:"description"`
	Dangerous    bool   `yaml:"dangerous"`
	LicenseGated string `yaml:"license_gated"`
}

type role struct {
	ID          string   `yaml:"id"`
	Description string   `yaml:"description"`
	Permissions []string `yaml:"permissions"`
}

const permsTpl = `// Code generated by scripts/gen-rbac.go from app/auth/permissions.yaml.
// DO NOT EDIT. Re-run via: make generate-rbac

package auth

// Permission is a stable, resource:action identifier.
type Permission string

// Permission constants. Hand-typed permission strings are forbidden by
// lint; use these constants in handler middleware and tests.
const (
{{- range .Permissions }}
	// {{ .Description }}
	{{ .GoConst }} Permission = "{{ .ID }}"
{{- end }}
)

// PermissionMeta is the per-permission metadata from the registry.
type PermissionMeta struct {
	ID           Permission
	Category     string
	Description  string
	Dangerous    bool
	LicenseGated string // feature id from license/features.yaml; "" if not gated
}

// Permissions maps every active permission id to its registry entry.
var Permissions = map[Permission]PermissionMeta{
{{- range .Permissions }}
	{{ .GoConst }}: {
		ID:           {{ .GoConst }},
		Category:     "{{ .Category }}",
		Description:  ` + "`" + `{{ .Description }}` + "`" + `,
		Dangerous:    {{ .Dangerous }},
		LicenseGated: "{{ .LicenseGated }}",
	},
{{- end }}
}

// permissionOrder preserves declaration order from permissions.yaml.
var permissionOrder = []Permission{
{{- range .Permissions }}
	{{ .GoConst }},
{{- end }}
}

// AllPermissions returns every active permission id in registry order.
func AllPermissions() []Permission {
	out := make([]Permission, len(permissionOrder))
	copy(out, permissionOrder)
	return out
}

// IsKnown reports whether p is a registered (non-deprecated) permission.
func IsKnown(p Permission) bool {
	_, ok := Permissions[p]
	return ok
}

// IsDangerous reports whether p is registered with dangerous=true.
func IsDangerous(p Permission) bool {
	if m, ok := Permissions[p]; ok {
		return m.Dangerous
	}
	return false
}

// LicenseGate returns the feature id required for p, or "" if p is not
// license-gated. Empty string for unknown permissions.
func LicenseGate(p Permission) string {
	if m, ok := Permissions[p]; ok {
		return m.LicenseGated
	}
	return ""
}

// Categories returns the registered category ids in declaration order.
func Categories() []string {
	return categoryOrder
}

var categoryOrder = []string{
{{- range .Categories }}
	"{{ .ID }}",
{{- end }}
}

// CategoryDescription returns the description for a category id, or "" if unknown.
func CategoryDescription(id string) string {
	return categoryDescriptions[id]
}

var categoryDescriptions = map[string]string{
{{- range .Categories }}
	"{{ .ID }}": ` + "`" + `{{ .Description }}` + "`" + `,
{{- end }}
}
`

const rolesTpl = `// Code generated by scripts/gen-rbac.go from app/auth/permissions.yaml.
// DO NOT EDIT. Re-run via: make generate-rbac

package auth

// RoleID is the stable identifier of a built-in or custom role.
type RoleID string

// Built-in role constants. Custom roles created at runtime use ad-hoc
// RoleID values; the registry only ships built-in role definitions.
const (
{{- range .Roles }}
	// {{ .Description }}
	{{ .GoConst }} RoleID = "{{ .ID }}"
{{- end }}
)

// RoleDefinition is a role and its effective permission set.
type RoleDefinition struct {
	ID          RoleID
	Description string
	Permissions []Permission
	IsBuiltIn   bool
}

// BuiltInRoles maps every built-in RoleID to its definition with
// category wildcards expanded at codegen time. Runtime callers do
// not need to interpret wildcards.
var BuiltInRoles = map[RoleID]RoleDefinition{
{{- range .Roles }}
	{{ .GoConst }}: {
		ID:          {{ .GoConst }},
		Description: ` + "`" + `{{ .Description }}` + "`" + `,
		IsBuiltIn:   true,
		Permissions: []Permission{
{{- range .ExpandedPermissions }}
			{{ . }},
{{- end }}
		},
	},
{{- end }}
}

// BuiltInRoleIDs returns the IDs of the built-in roles in declaration order.
func BuiltInRoleIDs() []RoleID {
	out := make([]RoleID, len(builtInRoleOrder))
	copy(out, builtInRoleOrder)
	return out
}

var builtInRoleOrder = []RoleID{
{{- range .Roles }}
	{{ .GoConst }},
{{- end }}
}
`

type permsCtx struct {
	Categories  []category
	Permissions []emitPerm
}

type emitPerm struct {
	ID           string
	GoConst      string
	Category     string
	Description  string
	Dangerous    bool
	LicenseGated string
}

type rolesCtx struct {
	Roles []emitRole
}

type emitRole struct {
	ID                  string
	GoConst             string
	Description         string
	ExpandedPermissions []string // Go-constant names, sorted
}

func main() {
	raw, err := os.ReadFile(inputPath)
	check(err, "read input")

	var reg registry
	check(yaml.Unmarshal(raw, &reg), "parse yaml")

	if len(reg.Permissions) == 0 {
		fail("no permissions in registry")
	}

	// Build permission lookup tables.
	permByID := make(map[string]permission, len(reg.Permissions))
	for _, p := range reg.Permissions {
		permByID[p.ID] = p
	}
	// Group permissions by category for wildcard expansion.
	byCategory := make(map[string][]string)
	for _, p := range reg.Permissions {
		byCategory[p.Category] = append(byCategory[p.Category], p.ID)
	}

	// Emit permissions.gen.go
	pCtx := permsCtx{
		Categories:  reg.Categories,
		Permissions: make([]emitPerm, 0, len(reg.Permissions)),
	}
	for _, p := range reg.Permissions {
		pCtx.Permissions = append(pCtx.Permissions, emitPerm{
			ID:           p.ID,
			GoConst:      toPascal(p.ID),
			Category:     p.Category,
			Description:  cleanDescription(p.Description),
			Dangerous:    p.Dangerous,
			LicenseGated: p.LicenseGated,
		})
	}
	writeFile(permissionsPath, permsTpl, pCtx, len(pCtx.Permissions), "permissions")

	// Emit roles.gen.go
	rolesByID := make(map[string]role, len(reg.Roles))
	for _, r := range reg.Roles {
		rolesByID[r.ID] = r
	}
	rCtx := rolesCtx{Roles: make([]emitRole, 0, len(reg.Roles))}
	for _, r := range reg.Roles {
		expanded := expandPermissions(r, rolesByID, byCategory, permByID, map[string]bool{})
		// Sort for determinism (spec C-07).
		sort.Strings(expanded)
		// De-duplicate.
		uniq := make([]string, 0, len(expanded))
		seen := map[string]bool{}
		for _, p := range expanded {
			if !seen[p] {
				uniq = append(uniq, p)
				seen[p] = true
			}
		}
		// Convert IDs to Go constants.
		goConsts := make([]string, 0, len(uniq))
		for _, id := range uniq {
			goConsts = append(goConsts, toPascal(id))
		}
		rCtx.Roles = append(rCtx.Roles, emitRole{
			ID:                  r.ID,
			GoConst:             "Role" + toPascalRole(r.ID),
			Description:         cleanDescription(r.Description),
			ExpandedPermissions: goConsts,
		})
	}
	writeFile(rolesPath, rolesTpl, rCtx, len(rCtx.Roles), "roles")
}

// expandPermissions resolves role-references (`<role-id>:*`), category
// wildcards (`<cat>:*`), and the bare `*` (admin) to explicit ids.
func expandPermissions(r role, rolesByID map[string]role, byCategory map[string][]string, permByID map[string]permission, visiting map[string]bool) []string {
	if visiting[r.ID] {
		fail("cyclic role inheritance involving %q", r.ID)
	}
	visiting[r.ID] = true
	defer delete(visiting, r.ID)

	out := []string{}
	for _, p := range r.Permissions {
		switch {
		case p == "*":
			// Admin wildcard — grant every permission.
			for id := range permByID {
				out = append(out, id)
			}
		case strings.HasSuffix(p, ":*"):
			prefix := strings.TrimSuffix(p, ":*")
			// Role inheritance takes precedence over category wildcards.
			if inherited, ok := rolesByID[prefix]; ok {
				out = append(out, expandPermissions(inherited, rolesByID, byCategory, permByID, visiting)...)
				continue
			}
			// Otherwise treat as category wildcard.
			ids, ok := byCategory[prefix]
			if !ok {
				fail("role %q references unknown wildcard %q", r.ID, p)
			}
			out = append(out, ids...)
		default:
			if _, ok := permByID[p]; !ok {
				fail("role %q grants unknown permission %q", r.ID, p)
			}
			out = append(out, p)
		}
	}
	return out
}

func writeFile(path, tplStr string, ctx interface{}, n int, label string) {
	tpl, err := template.New(label).Parse(tplStr)
	check(err, "parse template "+label)

	var buf bytes.Buffer
	check(tpl.Execute(&buf, ctx), "execute template "+label)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "%s\n---\n", buf.String())
		check(err, "gofmt "+label)
	}
	check(os.WriteFile(path, formatted, 0o644), "write "+label)
	fmt.Printf("wrote %s (%d %s)\n", path, n, label)
}

func toPascal(id string) string {
	// Permission ids are "resource:action"; the colon counts as a word
	// boundary. Underscores within tokens too. Result: HostConnectivityCheck.
	parts := strings.FieldsFunc(id, func(r rune) bool { return r == '_' || r == ':' })
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		b.WriteString(p[1:])
	}
	return b.String()
}

func toPascalRole(id string) string {
	parts := strings.FieldsFunc(id, func(r rune) bool { return r == '_' })
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		b.WriteString(p[1:])
	}
	return b.String()
}

func cleanDescription(s string) string {
	s = strings.TrimSpace(s)
	if s == "" {
		return ""
	}
	return strings.Join(strings.Fields(s), " ")
}

func check(err error, what string) {
	if err != nil {
		fail("%s: %v", what, err)
	}
}

func fail(format string, args ...interface{}) {
	fmt.Fprintf(os.Stderr, "gen-rbac: "+format+"\n", args...)
	os.Exit(1)
}
