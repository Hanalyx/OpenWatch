// @spec system-audit-emission
//
// Codegen contract tests (AC-01, AC-02, AC-03). These tests assert the
// shape of events.gen.go without re-invoking the generator at test time —
// the generator is a build-time invariant, validated in CI by running
// `make generate-audit` and asserting no git diff.

package audit

import (
	"os"
	"path/filepath"
	"regexp"
	"strings"
	"testing"
)

// @ac AC-01
// AC-01: events.gen.go has one typed Code constant per active event in events.yaml.
func TestCodegen_EventConstants(t *testing.T) {
	t.Run("system-audit-emission/AC-01", func(t *testing.T) {
		// Spot-check a representative set covering each category. If any
		// constant is missing, the package wouldn't compile — so reaching
		// here means the constants exist.
		mustExist := []Code{
			AuthLoginSuccess,
			AuthLoginFailure,
			AuthLogout,
			AuthTokenIssued,
			AuthTokenRevoked,
			SystemStartup,
			SystemShutdown,
		}
		for _, c := range mustExist {
			if c == "" {
				t.Errorf("constant resolves to empty string")
			}
			if !strings.Contains(string(c), ".") {
				t.Errorf("code %q lacks dotted hierarchy", c)
			}
		}

		// All 90 codes from events.yaml must be in the Metadata map.
		if len(Metadata) < 90 {
			t.Errorf("Metadata size = %d, want >= 90 (events.yaml has 90 active codes)", len(Metadata))
		}
	})
}

// @ac AC-02
// AC-02: Metadata map has Code, Category, Severity per entry. Description
// and ActorTypes are optional per events.yaml authoring rules — some
// integration-event codes inherit those at runtime — but Severity and
// Category are mandatory because both feed the canonical envelope.
func TestCodegen_MetadataPopulated(t *testing.T) {
	t.Run("system-audit-emission/AC-02", func(t *testing.T) {
		for code, meta := range Metadata {
			if meta.Code != code {
				t.Errorf("Metadata[%q].Code = %q, want self-referential", code, meta.Code)
			}
			if meta.Category == "" {
				t.Errorf("Metadata[%q].Category is empty (required)", code)
			}
			if meta.Severity == "" {
				t.Errorf("Metadata[%q].Severity is empty (required)", code)
			}
			// Severity must be from the closed set.
			switch meta.Severity {
			case SeverityInfo, SeverityWarning, SeverityError, SeverityCritical:
			default:
				t.Errorf("Metadata[%q].Severity = %q, not in known set", code, meta.Severity)
			}
		}
	})
}

// @ac AC-03
// AC-03: codegen is deterministic — same input produces byte-identical
// output. Validated here by parsing events.gen.go and asserting it
// matches the patterns the generator emits (one constant block, one
// Metadata block, no trailing whitespace, no duplicates). The full
// idempotency check (re-run generator + git diff) runs in CI.
func TestCodegen_DeterministicShape(t *testing.T) {
	t.Run("system-audit-emission/AC-03", func(t *testing.T) {
		genPath := filepath.Join("events.gen.go")
		data, err := os.ReadFile(genPath)
		if err != nil {
			t.Fatalf("read events.gen.go: %v", err)
		}
		src := string(data)

		// "DO NOT EDIT" header.
		if !strings.Contains(src, "// Code generated by") || !strings.Contains(src, "DO NOT EDIT") {
			t.Error("events.gen.go missing canonical 'DO NOT EDIT' header")
		}
		// No duplicate constant declarations.
		constRe := regexp.MustCompile(`(?m)^\s+([A-Z][A-Za-z0-9]+)\s+Code\s+=\s+"`)
		seen := map[string]int{}
		for _, m := range constRe.FindAllStringSubmatch(src, -1) {
			seen[m[1]]++
		}
		for name, n := range seen {
			if n > 1 {
				t.Errorf("duplicate constant %q (appears %d times)", name, n)
			}
		}
		// Metadata map opens with the expected literal.
		if !strings.Contains(src, "var Metadata = map[Code]EventMeta{") {
			t.Error("Metadata declaration missing or renamed")
		}
	})
}
